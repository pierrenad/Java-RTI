    private void validerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerButtonActionPerformed
        // TODO add your handling code here:
        RequeteProtocol req = new RequeteProtocol(RequeteProtocol.LOGIN, loginTxt.getText().concat("#" + String.valueOf(pwdTxt.getPassword())));
        
        Network n = new Network();
        cSock = n.Init(); 
        n.SendRequest(cSock, req); 
        
        ReponseProtocol rep = null;
        try {
            ois = new ObjectInputStream(cSock.getInputStream()); 
            rep = (ReponseProtocol)ois.readObject(); // va pas 
            System.out.println("<LoginWindow> *** Reponse re√ßue");
            
            if(rep.getCode() == ReponseProtocol.LOGIN_OK) {
                CustomerWindow cw = new CustomerWindow(); 
                cw.setVisible(true); 
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(null, "Connexion incorrecte !", "Petit filou !", JOptionPane.ERROR_MESSAGE); 
            }
        }
        catch (Exception e) { 
            System.err.println("<LoginWindow> " + e.getMessage()); 
            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_validerButtonActionPerformed

